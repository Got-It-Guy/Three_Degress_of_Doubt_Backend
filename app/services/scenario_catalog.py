def get_primary_clue(stage_genre: str, evidence_name: str) -> str:
    methods = get_stage_methods(stage_genre)
    clues = methods.get(evidence_name)
    if not clues:
        raise KeyError(f"알 수 없는 핵심 단서입니다: {stage_genre} / {evidence_name}")
    return clues[0]


def get_hide_fallback(stage_genre: str) -> str:
    return GENRE_HIDE_FALLBACKS.get(stage_genre, "네, 상황부터 차근히 말씀드릴게요.")


def get_reveal_fallback(stage_genre: str, evidence_name: str, ai_name: str | None = None) -> str:
    clue = get_primary_clue(stage_genre, evidence_name)
    speaker = f"{ai_name}입니다. " if ai_name else ""

